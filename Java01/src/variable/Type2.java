package variable;

public class Type2 {
	public static void main(String[] args) {
		// 변수를 활용해서
		// 이름, 출생년도, 키
		
		
		// 예시의 OOO을 변수로 활용
		/*
		 이름 : OOO
		 출생년도 : OOOO년
		 키 : OOO.OOcm
		 */
		String 변수1;
		int 변수2;
		double 변수3;
		
		변수1 = "김순표";
		변수2 = 1999;
		변수3 = 176.50;
		
		System.out.println("이름 : "+변수1);
		System.out.println("출생년도 : "+변수2+"년");
		System.out.println("키 : "+변수3+"cm");
		
	}
}
